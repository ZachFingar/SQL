    private void getAppListButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getAppListButtonActionPerformed
        String name = nameTextField.getText();
        String email = emailTextField.getText();
        Boolean searchResult = false;
        if (name.equals("") && email.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill out name and email fields first");
        } else {
            List<String> appointments = new ArrayList<>();
            String state = "SELECT `client`, `clientemail`, appointment_id\n"
                    + "FROM appointment";
            try {
                PreparedStatement query;
                query = this.mainFrame.myManager.getConn().prepareStatement(state);
                ResultSet rs = query.executeQuery();
                while (rs.next()) {
                    String c = rs.getString(1);
                    String e = rs.getString(2);
                    if ((c.equals(name)) && (e.equals(email))) {
                        appointments.add(rs.getString(3));
                        searchResult = true;
                    }
                }
            } catch (SQLException ex) {System.out.println(ex);}
            if (searchResult) {
                viewAppListComboBox.setModel(new DefaultComboBoxModel(appointments.toArray()));
            } else {
                JOptionPane.showMessageDialog(null, "No appointment found, please try again");
            }
        }
    }//GEN-LAST:event_getAppListButtonActionPerformed

    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        try {
            int rating = (int) ratingSpinner.getValue();
            String name = nameTextField.getText();
            String email = emailTextField.getText();
            String aid = (String) viewAppListComboBox.getSelectedItem();
            String comment = commentTextArea.getText();

            
            String values = aid + ",'" + name + "', '" + email + "', " + rating + ", '" + comment + "'";
            if (("".equals(name)) || ("".equals(email)) || ("Please get previous appointments first".equals(aid))) {
                JOptionPane.showMessageDialog(null, "Please Fill in all fields marked with a *.");
            } else {
                String statement = "INSERT INTO evaluation (aid, client, clientemail, rating, comment_text) VALUES (" + values + ")";
                PreparedStatement pst = this.mainFrame.myManager.getConn().prepareStatement(statement);
                this.mainFrame.getLogTextArea().append("\n"+pst.toString());
                this.mainFrame.myManager.runUpdate(statement);
                JOptionPane.showMessageDialog(null, "Your evaluation has been saved.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Appointment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_submitButtonActionPerformed

