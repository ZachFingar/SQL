    private void requestButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestButtonActionPerformed
        try {
            int duration = (int) durationSpinner.getValue();
            String counselor = (String) counselorComboBox.getSelectedItem();
            int counselorID = counselorComboBox.getSelectedIndex();
            String subject = (String) subjectComboBox.getSelectedItem();
            String room = (String) locationComboBox.getSelectedItem();
            String email = emailTextField.getText();
            String name = nameTextField.getText();
            String phone = phoneTextField.getText();
            
            Calendar calendar = dateChooserCombo1.getSelectedDate();
            Date date = calendar.getTime();
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
            String strDate = formatter.format(date);
            
            String subjectName = (String) subjectComboBox.getSelectedItem();
            PreparedStatement pst = this.mainFrame.getMyManager().getConn().prepareStatement("select subject_id from subject where name=?");
            pst.setString(1, subjectName);
            ResultSet rs = pst.executeQuery();
            rs.next();
            int subjectID = rs.getInt("subject_id");
            
            String values = counselorID + "," + subjectID + ", '" + name + "', '" + phone + "', '" + email + "', '" + room + "', '" + strDate + "', " + duration;
            System.out.println(values);
            if (("".equals(name)) || ("   -   -    ".equals(phone)) || ("".equals(email))) {
                JOptionPane.showMessageDialog(null, "Please Fill in all fields.");
            } else if (("Select a counselor".equals(counselor)) || ("Select a counselor first".equals(subject))) {
                JOptionPane.showMessageDialog(null, "Please Select a counselor and subject.");
            } else {
                String statement = "INSERT INTO appointment (cid, sid, client, clientphone, clientemail, location, starttime, duration) VALUES (" + values + ")";
                pst = this.mainFrame.myManager.getConn().prepareStatement(statement);
                this.mainFrame.getLogTextArea().append("\n"+pst.toString());
                this.mainFrame.myManager.runUpdate(statement);
                JOptionPane.showMessageDialog(null, "Your Appointment has been requested.");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Appointment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_requestButtonActionPerformed

