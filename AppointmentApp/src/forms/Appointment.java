    private void counselorComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_counselorComboBoxActionPerformed
        if (counselorComboBox.getSelectedIndex() != -1) {
            int name = counselorComboBox.getSelectedIndex();

            List<String> subjects = new ArrayList<>();
            subjects.add("Select a counselor first");
            PreparedStatement statement = null;
            String query = "SELECT name \n"
                    + "FROM subject \n"
                    + "WHERE counselor_id = ?";
            try {
                statement = this.mainFrame.myManager.getConn().prepareStatement(query);
                statement.setInt(1, name);
                ResultSet rs = statement.executeQuery();
                while (rs.next()) {
                    subjects.add(rs.getString("name"));
                }
                subjectComboBox.setModel(new DefaultComboBoxModel(subjects.toArray()));
            } catch (SQLException e) {
                System.out.println(e);
            }
        }


    }//GEN-LAST:event_counselorComboBoxActionPerformed

