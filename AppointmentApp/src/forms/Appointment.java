    public void fillCombo(Connection conn) {
        List<String> counselors = new ArrayList<>();
        counselors.add("Select a counselor");
        Statement statement = null;
        String query = "SELECT first_name, last_name \n"
                + "FROM counselor.counselor";
        try {
            statement = conn.createStatement();
            ResultSet rs = statement.executeQuery(query);
            while (rs.next()) {
                counselors.add(rs.getString("first_name") + " " + rs.getString("last_name"));
            }
            counselorComboBox.setModel(new DefaultComboBoxModel(counselors.toArray()));
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

